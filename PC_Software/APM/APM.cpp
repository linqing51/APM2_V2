// CAPMApp 初始化

BOOL CAPMApp::InitInstance()
{
	HANDLE hMutex = CreateMutex(NULL, TRUE, m_pszAppName);
	if (GetLastError() == ERROR_ALREADY_EXISTS) { ::AfxMessageBox(_T("只允许运行一个程序,不可重复启动")); return FALSE; }

	// 如果一个运行在 Windows XP 上的应用程序清单指定要
	// 使用 ComCtl32.dll 版本 6 或更高版本来启用可视化方式，
	//则需要 InitCommonControlsEx()。  否则，将无法创建窗口。
	INITCOMMONCONTROLSEX InitCtrls;
	InitCtrls.dwSize = sizeof(InitCtrls);
	// 将它设置为包括所有要在应用程序中使用的
	// 公共控件类。
	InitCtrls.dwICC = ICC_WIN95_CLASSES;
	InitCommonControlsEx(&InitCtrls);

	CBCGPWinApp::InitInstance();


	// 初始化 OLE 库
	if (!AfxOleInit())
	{
		AfxMessageBox(IDP_OLE_INIT_FAILED);
		return FALSE;
	}

	AfxEnableControlContainer();

	EnableTaskbarInteraction(FALSE);

	// 使用 RichEdit 控件需要  AfxInitRichEdit2()	
	 AfxInitRichEdit2();

	// 标准初始化
	// 如果未使用这些功能并希望减小
	// 最终可执行文件的大小，则应移除下列
	// 不需要的特定初始化例程
	// 更改用于存储设置的注册表项
	// TODO:  应适当修改该字符串，
	// 例如修改为公司或组织名
	SetRegistryKey(_T("LYPE\\APM"));
	LoadStdProfileSettings(2);  // 加载标准 INI 文件选项(包括 MRU)


	// 注册应用程序的文档模板。  文档模板
	// 将用作文档、框架窗口和视图之间的连接
	CSingleDocTemplate* pDocTemplate;
	pDocTemplate = new CSingleDocTemplate(
		IDR_MAINFRAME,
		RUNTIME_CLASS(CAPMDoc),
		RUNTIME_CLASS(CMainFrame),       // 主 SDI 框架窗口
		RUNTIME_CLASS(CAPMView));
	if (!pDocTemplate)
		return FALSE;
	AddDocTemplate(pDocTemplate);


	// 分析标准 shell 命令、DDE、打开文件操作的命令行
	m_nCmdShow = SW_HIDE;

	CCommandLineInfo cmdInfo;
	ParseCommandLine(cmdInfo);



	// 调度在命令行中指定的命令。  如果
	// 用 /RegServer、/Register、/Unregserver 或 /Unregister 启动应用程序，则返回 FALSE。
	if (!ProcessShellCommand(cmdInfo))
		return FALSE;
	if (((CMainFrame*)m_pMainWnd)->InitialDefine())
	{
 #ifndef _DEBUG
		m_pMainWnd->ShowWindow(SW_HIDE);

	CLoginDlg dlg;
	INT_PTR nResponse = dlg.DoModal();
	if (nResponse == IDCANCEL)
		return FALSE;
 #endif
	if (!((CMainFrame*)m_pMainWnd)->InitHardware())
		return FALSE;
	}
	else
		return FALSE;

	// 唯一的一个窗口已初始化，因此显示它并对其进行更新
// 	m_pMainWnd->ShowWindow(SW_SHOWMAXIMIZED);
	m_pMainWnd->UpdateWindow();
	::PostMessage(m_pMainWnd->m_hWnd, WM_USER_UPDATEUI, 2, 0);
	::PostMessage(m_pMainWnd->m_hWnd, WM_USER_UPDATEUI, 3, 0);

	return TRUE;
}

